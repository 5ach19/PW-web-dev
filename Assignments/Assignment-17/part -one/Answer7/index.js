// Q7. Write a program in JavaScript to calculate the area of a circle given its radius value of 10. Use appropriate arithmetic operators.

const radius = 10
const pi = Math.PI

const areaOfCircle = pi * (radius * radius)


console.log(`the area of circle given radius ${radius} is ${areaOfCircle}`);
console.log(`the area of circle given radius ${radius} is ${areaOfCircle.toFixed(2)}`); //use to fixed -> getting 2 value after decimal